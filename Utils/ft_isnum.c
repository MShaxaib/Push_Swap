#include "Utils.h"

int	ft_isnum(char *a)
{
	int	i;

	i = 0;
	while ((a[i] == '+' || a[i] == '-') && a[i] != '\0')
		i++;
	if (a[i] == '\0')
		return (1);
	while (a[i] != '\0')
	{
		if (!(a[i] >= '0' && a[i] <= '9'))
			return (1);
		i++;
	}
	return (0);
}
 